l = float(input("Masukan Nilai Panjang Gelombang (m) : "))
f = float(input("Masukan Nilai Frekuensi (Hz) : "))

# Mengkonversi
v = l * f

# Menampilkan Hasil
print('==========================================================')
print('Maka Nilai Cepat Rambat (m/s) = ',round(v,3))
